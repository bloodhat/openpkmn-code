    public static int statFormula(int base, int dv, int level, int statExp) {
	float fBase = base;
	float fDV = dv;
	float fLevel = level;
	float fStatExp = statExp;

	return (int)((fLevel * (fBase + fDV +
			       (fStatExp / 2048.0)) / 50.0) + 5.0);
    }

    public static int hpFormula(int base, int dv, int level, int statExp) {
	float fBase = base;
	float fDV = dv;
	float fLevel = level;
	float fStatExp = statExp;

	return (int)((fLevel * (fBase + fDV + (fStatExp / 2048.0) +
				50.0) / 50.0) + 10.0);
    }
    
    public static int[] getRandomDVs() {
	int[] dvs = new int[4];
	for(int i = 0; i < dvs.length; i++) {
	    dvs[i] = (int)(Math.random() * 16);
	}
	return dvs;
    }

    public static int[] getMaxDVs() {
	int[] dvs = new int[4];
	for(int i = 0; i < dvs.length; i++) {
	    dvs[i] = 15;
	}
	return dvs;
    }

    public static int[] getMaxStatExp() {
	int[] statExp =
	    {65535, 65535, 65535, 65535, 65535};
	return statExp;
    }

